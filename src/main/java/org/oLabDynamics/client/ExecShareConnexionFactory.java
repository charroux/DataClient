package org.oLabDynamics.client;

public class ExecShareConnexionFactory {
	
	String serverEntryPoint;
	String userName;
	String password;
	
	public String getServerEntryPoint() {
		return serverEntryPoint;
	}

	public void setServerEntryPoint(String serverEntryPoint) {
		this.serverEntryPoint = serverEntryPoint;
	}

	public String getUserName() {
		return userName;
	}
	
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setPassword(String password) {
		this.password = password;
	}
	
	

}
