package jra.model;

public class Choice {
	
	Object[] choices;
	Object value;
	
	public Object[] getChoices() {
		return choices;
	}
	public void setChoices(Object[] choices) {
		this.choices = choices;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
	
	

}
